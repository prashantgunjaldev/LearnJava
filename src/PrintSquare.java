public class PrintSquare {
    public static void main(String arg[]){

        int size = 3;
        System.out.println("\n============ Square Printing  =============");
        for(int i = 1; i <= size; i++){
            for(int j = 1; j<=size; j++){
                System.out.print("* ");
            }
            System.out.print("\n");
        }
    }
}

