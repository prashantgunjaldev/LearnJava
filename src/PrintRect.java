public class PrintRect {
    public static void main(String arg[]){

        int height = 3;
        int width = 6;
        System.out.println("\n============ Print Rectangle  =============");
        for(int i = 1; i <= height; i++){
            for(int j = 1; j<=width; j++){
                System.out.print("* ");
            }
            System.out.print("\n");
        }
    }
}

